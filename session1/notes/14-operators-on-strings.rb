=== String operators

  # You can use basic operators on Strings,
  # just like other objects:

  my_string   = "Face"
  your_string = "book"

  my_string + your_string  
  # => "Facebook"

  my_string == your_string 
  # => false
  
  my_string = "book"
  # => "book"  # sometimes we will show this
               # but most of the time not. it's hi hi
  
  my_string == your_string 
  # => true
  
